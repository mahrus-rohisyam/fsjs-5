const penjumlahanTanpa = (x, y) => {
  x + y
}

const penjumlahanReturn = (a, b) => {
  return a + b
}

penjumlahanTanpa(1, 2)                    // No result in terminal
penjumlahanReturn(1, 2)                   // No result in terminal

console.log(penjumlahanTanpa(1, 2));      // No result in terminal
console.log(penjumlahanReturn(1, 2));     // Result showed in terminal